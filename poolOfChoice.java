public class poolOfChoice
{
    String word;
    int score;

    public poolOfChoice(String word, int score) {
        this.word = word;
        this.score = score;
    }

    public void setWord(String word) {
        this.word = word;
    }

    public String getWord() {
        return word;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
